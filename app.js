    renderPortfolioProjects();
    
    /* 1. Header scroll visual effect */
    const header = document.querySelector('.glass-header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    /* 2. Responsive Mobile Menu Toggle */
    const menuToggle = document.querySelector('.mobile-menu-toggle');
    const mobileNav = document.querySelector('.mobile-nav');
    
    menuToggle.addEventListener('click', () => {
        menuToggle.classList.toggle('active');
        mobileNav.classList.toggle('open');
        
        // Animated Menu Lines
        const spans = menuToggle.querySelectorAll('span');
        if (menuToggle.classList.contains('active')) {
            spans[0].style.transform = 'rotate(45deg) translate(5px, 5px)';
            spans[1].style.opacity = '0';
            spans[2].style.transform = 'rotate(-45deg) translate(7px, -7px)';
        } else {
            spans[0].style.transform = 'none';
            spans[1].style.opacity = '1';
            spans[2].style.transform = 'none';
        }
    });
    
    // Close Mobile Menu on link click
    document.querySelectorAll('.mobile-link').forEach(link => {
        link.addEventListener('click', () => {
            menuToggle.classList.remove('active');
            mobileNav.classList.remove('open');
            menuToggle.querySelectorAll('span').forEach(s => s.style.transform = 'none');
            menuToggle.querySelectorAll('span')[1].style.opacity = '1';
        });
    });

    /* 3. Dark & Light Theme Switch Logic */
    const themeToggle = document.getElementById('theme-toggle');
    const sunIcon = document.querySelector('.sun-icon');
    const moonIcon = document.querySelector('.moon-icon');
    
    // Retrieve theme preference
    const savedTheme = localStorage.getItem('portfolio-theme') || 'dark';
    if (savedTheme === 'light') {
        body.classList.remove('dark-theme');
        body.classList.add('light-theme');
        sunIcon.style.display = 'none';
        moonIcon.style.display = 'block';
    }
    
    themeToggle.addEventListener('click', () => {
        body.classList.toggle('dark-theme');
        body.classList.toggle('light-theme');
        
        if (body.classList.contains('light-theme')) {
            sunIcon.style.display = 'none';
            moonIcon.style.display = 'block';
            localStorage.setItem('portfolio-theme', 'light');
        } else {
            sunIcon.style.display = 'block';
            moonIcon.style.display = 'none';
            localStorage.setItem('portfolio-theme', 'dark');
        }
    });

    /* 4. Language Translation Logic with Smooth Fade Out/In */
    const langToggle = document.getElementById('lang-toggle');
    
    langToggle.addEventListener('click', () => {
        currentLang = currentLang === 'ar' ? 'en' : 'ar';
        
        // Update Toggle Button Text
        langToggle.querySelector('.lang-text').textContent = currentLang === 'ar' ? 'EN' : 'AR';
        
        // Apply transitions
        document.documentElement.classList.add('lang-changing');
        
        // Animate elements fade out
        const translatableElements = document.querySelectorAll('[data-i18n]');
        translatableElements.forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateY(10px)';
            el.style.transition = 'opacity 0.2s ease, transform 0.2s ease';
        });
        
        setTimeout(() => {
            // Swap Language and Direction
            if (currentLang === 'en') {
                document.documentElement.setAttribute('lang', 'en');
                document.documentElement.setAttribute('dir', 'ltr');
            } else {
                document.documentElement.setAttribute('lang', 'ar');
                document.documentElement.setAttribute('dir', 'rtl');
            }
            
            // Rewrite Translation Contents
            translatableElements.forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (translations[currentLang] && translations[currentLang][key]) {
                    el.innerHTML = translations[currentLang][key];
                }
            });
            
            // Re-render portfolio projects with active language
            renderPortfolioProjects();
            setupLightboxEvents(); // Re-bind dynamic click listeners
            
            // Adjust Form placeholders dynamically
            const nameInput = document.getElementById('name');
            const emailInput = document.getElementById('email');
            const msgInput = document.getElementById('message');
            
            if (currentLang === 'en') {
                if (nameInput) nameInput.placeholder = "Enter your full name";
                if (emailInput) emailInput.placeholder = "name@domain.com";
                if (msgInput) msgInput.placeholder = "How can I help you today?";
            } else {
                if (nameInput) nameInput.placeholder = "أدخل اسمك الكامل";
                if (emailInput) emailInput.placeholder = "name@domain.com";
                if (msgInput) msgInput.placeholder = "كيف يمكنني مساعدتك اليوم؟";
            }
            
            // Fade back in
            setTimeout(() => {
                translatableElements.forEach(el => {
                    el.style.opacity = '1';
                    el.style.transform = 'translateY(0)';
                });
                document.documentElement.classList.remove('lang-changing');
            }, 50);
            
        }, 200);
    });

    /* 5. Dynamic Portfolio Grid Filtering */
    const filterButtons = document.querySelectorAll('.filter-btn');
    
    filterButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            // Toggle active filter state
            filterButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            const filterValue = btn.getAttribute('data-filter');
            const portfolioItems = document.querySelectorAll('.portfolio-item');
            
            portfolioItems.forEach(item => {
                item.style.opacity = '0';
                item.style.transform = 'scale(0.95)';
                item.style.transition = 'opacity 0.4s ease, transform 0.4s ease';
                
                setTimeout(() => {
                    if (filterValue === 'all' || item.getAttribute('data-category') === filterValue) {
                        item.classList.remove('hidden');
                        setTimeout(() => {
                            item.style.opacity = '1';
                            item.style.transform = 'scale(1)';
                        }, 50);
                    } else {
                        item.classList.add('hidden');
                    }
                }, 400);
            });
        });
    });

