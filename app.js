/* ==========================================================================
   DOM Elements Activation & Setup
   ========================================================================== */
document.addEventListener('DOMContentLoaded', () => {
    
    // Core Elements
    const body = document.body;
    const currentYearSpan = document.getElementById('current-year');
    
    // Set Footer Year
    if (currentYearSpan) {
        currentYearSpan.textContent = new Date().getFullYear();
    }

    // Dynamic Portfolio Projects Rendering
    renderPortfolioProjects();
    
    /* 1. Header scroll visual effect */
    const header = document.querySelector('.glass-header');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    /* 2. Responsive Mobile Menu Toggle */
    const menuToggle = document.querySelector('.mobile-menu-toggle');
    const mobileNav = document.querySelector('.mobile-nav');
    
    menuToggle.addEventListener('click', () => {
        menuToggle.classList.toggle('active');
        mobileNav.classList.toggle('open');
        
        // Animated Menu Lines
        const spans = menuToggle.querySelectorAll('span');
        if (menuToggle.classList.contains('active')) {
            spans[0].style.transform = 'rotate(45deg) translate(5px, 5px)';
            spans[1].style.opacity = '0';
            spans[2].style.transform = 'rotate(-45deg) translate(7px, -7px)';
        } else {
            spans[0].style.transform = 'none';
            spans[1].style.opacity = '1';
            spans[2].style.transform = 'none';
        }
    });
    
    // Close Mobile Menu on link click
    document.querySelectorAll('.mobile-link').forEach(link => {
        link.addEventListener('click', () => {
            menuToggle.classList.remove('active');
            mobileNav.classList.remove('open');
            menuToggle.querySelectorAll('span').forEach(s => s.style.transform = 'none');
            menuToggle.querySelectorAll('span')[1].style.opacity = '1';
        });
    });

    /* 3. Dark & Light Theme Switch Logic */
    const themeToggle = document.getElementById('theme-toggle');
    const sunIcon = document.querySelector('.sun-icon');
    const moonIcon = document.querySelector('.moon-icon');
    
    // Retrieve theme preference
    const savedTheme = localStorage.getItem('portfolio-theme') || 'dark';
    if (savedTheme === 'light') {
        body.classList.remove('dark-theme');
        body.classList.add('light-theme');
        sunIcon.style.display = 'none';
        moonIcon.style.display = 'block';
    }
    
    themeToggle.addEventListener('click', () => {
        body.classList.toggle('dark-theme');
        body.classList.toggle('light-theme');
        
        if (body.classList.contains('light-theme')) {
            sunIcon.style.display = 'none';
            moonIcon.style.display = 'block';
            localStorage.setItem('portfolio-theme', 'light');
        } else {
            sunIcon.style.display = 'block';
            moonIcon.style.display = 'none';
            localStorage.setItem('portfolio-theme', 'dark');
        }
    });

    /* 4. Language Translation Logic with Smooth Fade Out/In */
    const langToggle = document.getElementById('lang-toggle');
    
    langToggle.addEventListener('click', () => {
        currentLang = currentLang === 'ar' ? 'en' : 'ar';
        
        // Update Toggle Button Text
        langToggle.querySelector('.lang-text').textContent = currentLang === 'ar' ? 'EN' : 'AR';
        
        // Apply transitions
        document.documentElement.classList.add('lang-changing');
        
        // Animate elements fade out
        const translatableElements = document.querySelectorAll('[data-i18n]');
        translatableElements.forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateY(10px)';
            el.style.transition = 'opacity 0.2s ease, transform 0.2s ease';
        });
        
        setTimeout(() => {
            // Swap Language and Direction
            if (currentLang === 'en') {
                document.documentElement.setAttribute('lang', 'en');
                document.documentElement.setAttribute('dir', 'ltr');
            } else {
                document.documentElement.setAttribute('lang', 'ar');
                document.documentElement.setAttribute('dir', 'rtl');
            }
            
            // Rewrite Translation Contents
            translatableElements.forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (translations[currentLang] && translations[currentLang][key]) {
                    el.innerHTML = translations[currentLang][key];
                }
            });
            
            // Re-render portfolio projects with active language
            renderPortfolioProjects();
            setupLightboxEvents(); // Re-bind dynamic click listeners
            
            // Adjust Form placeholders dynamically
            const nameInput = document.getElementById('name');
            const emailInput = document.getElementById('email');
            const msgInput = document.getElementById('message');
            
            if (currentLang === 'en') {
                if (nameInput) nameInput.placeholder = "Enter your full name";
                if (emailInput) emailInput.placeholder = "name@domain.com";
                if (msgInput) msgInput.placeholder = "How can I help you today?";
            } else {
                if (nameInput) nameInput.placeholder = "أدخل اسمك الكامل";
                if (emailInput) emailInput.placeholder = "name@domain.com";
                if (msgInput) msgInput.placeholder = "كيف يمكنني مساعدتك اليوم؟";
            }
            
            // Fade back in
            setTimeout(() => {
                translatableElements.forEach(el => {
                    el.style.opacity = '1';
                    el.style.transform = 'translateY(0)';
                });
                document.documentElement.classList.remove('lang-changing');
            }, 50);
            
        }, 200);
    });

    /* 5. Dynamic Portfolio Grid Filtering */
    const filterButtons = document.querySelectorAll('.filter-btn');
    
    filterButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            // Toggle active filter state
            filterButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            
            const filterValue = btn.getAttribute('data-filter');
            const portfolioItems = document.querySelectorAll('.portfolio-item');
            
            portfolioItems.forEach(item => {
                item.style.opacity = '0';
                item.style.transform = 'scale(0.95)';
                item.style.transition = 'opacity 0.4s ease, transform 0.4s ease';
                
                setTimeout(() => {
                    if (filterValue === 'all' || item.getAttribute('data-category') === filterValue) {
                        item.classList.remove('hidden');
                        setTimeout(() => {
                            item.style.opacity = '1';
                            item.style.transform = 'scale(1)';
                        }, 50);
                    } else {
                        item.classList.add('hidden');
                    }
                }, 400);
            });
        });
    });

    /* 6. Testimonials Slider Mechanics */
    const testimonialCards = document.querySelectorAll('.testimonial-card');
    const prevBtn = document.getElementById('prev-testi');
    const nextBtn = document.getElementById('next-testi');
    let currentSlide = 0;
    
    function showSlide(index) {
        testimonialCards.forEach(c => c.classList.remove('active'));
        currentSlide = (index + testimonialCards.length) % testimonialCards.length;
        testimonialCards[currentSlide].classList.add('active');
    }
    
    if (prevBtn && nextBtn) {
        prevBtn.addEventListener('click', () => showSlide(currentSlide - 1));
        nextBtn.addEventListener('click', () => showSlide(currentSlide + 1));
        
        // Auto slide change
        setInterval(() => {
            showSlide(currentSlide + 1);
        }, 8000);
    }

    /* 7. Interactive Project Lightbox Setup */
    setupLightboxEvents();
    
    const lightbox = document.getElementById('project-lightbox');
    const closeBtn = document.querySelector('.lightbox-close-btn');
    const lightboxBackdrop = document.querySelector('.lightbox-backdrop');
    
    function closeLightbox() {
        lightbox.classList.remove('open');
        body.style.overflow = 'auto'; // Unlock background scroll
        // Clear mediaContainer to immediately stop video playback
        const mediaContainer = document.querySelector('.lightbox-media');
        if (mediaContainer) {
            mediaContainer.innerHTML = '';
        }
    }
    
    if (closeBtn && lightboxBackdrop) {
        closeBtn.addEventListener('click', closeLightbox);
        lightboxBackdrop.addEventListener('click', closeLightbox);
    }
    
    // Close Lightbox with Escape key
    window.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && lightbox.classList.contains('open')) {
            closeLightbox();
        }
    });

    /* 8. Scroll to Top Button Activation */
    const scrollToTopBtn = document.getElementById('scroll-to-top');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 400) {
            scrollToTopBtn.classList.add('show');
        } else {
            scrollToTopBtn.classList.remove('show');
        }
    });
    
    if (scrollToTopBtn) {
        scrollToTopBtn.addEventListener('click', () => {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
    }

    /* 9. Visual Reveal-on-Scroll System */
    const reveals = document.querySelectorAll('.reveal');
    function checkReveal() {
        const activeReveals = document.querySelectorAll('.reveal');
        activeReveals.forEach(r => {
            const windowHeight = window.innerHeight;
            const elementTop = r.getBoundingClientRect().top;
            const elementVisible = 100;
            
            if (elementTop < windowHeight - elementVisible) {
                r.classList.add('active');
            }
        });
    }
    
    window.addEventListener('scroll', checkReveal);
    
    // Initialize elements immediately visible
    setTimeout(checkReveal, 300);

    // Apply reveal class to sections (except hero) for smooth scrolling animations
    document.querySelectorAll('section:not(#hero), .stats-glass').forEach(el => {
        el.classList.add('reveal');
    });

    /* 10. Contact Form with Interactive Loading & Feedback */
    const contactForm = document.getElementById('portfolio-contact-form');
    const successFeedback = document.getElementById('form-feedback-success');
    const errorFeedback = document.getElementById('form-feedback-error');
    
    if (contactForm) {
        contactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            
            const submitBtn = contactForm.querySelector('button[type="submit"]');
            const btnText = submitBtn.querySelector('.submit-btn-text');
            const spinner = submitBtn.querySelector('.submit-spinner');
            
            // Hide previous alerts
            successFeedback.style.display = 'none';
            errorFeedback.style.display = 'none';
            
            // Trigger Loading state
            btnText.style.display = 'none';
            spinner.style.display = 'block';
            submitBtn.disabled = true;
            
            // Simulate API request
            setTimeout(() => {
                spinner.style.display = 'none';
                btnText.style.display = 'block';
                submitBtn.disabled = false;
                
                // Show visual checkmark
                successFeedback.style.display = 'block';
                successFeedback.style.opacity = '0';
                successFeedback.style.transform = 'translateY(10px)';
                successFeedback.style.transition = 'all 0.5s ease';
                
                setTimeout(() => {
                    successFeedback.style.opacity = '1';
                    successFeedback.style.transform = 'translateY(0)';
                }, 50);
                
                contactForm.reset(); // Reset form values
                
                // Remove success checkmark after 6 seconds
                setTimeout(() => {
                    successFeedback.style.opacity = '0';
                    setTimeout(() => { successFeedback.style.display = 'none'; }, 500);
                }, 6000);
                
            }, 1800);
        });
    }
});

