/* ==========================================================================
   12. Interactive Particle Canvas Background Engine
   ========================================================================== */
const canvas = document.getElementById('particle-canvas');
const ctx = canvas.getContext('2d');

let particlesArray = [];
const mouse = {
    x: null,
    y: null,
    radius: 120
};

// Handle window resizing
window.addEventListener('resize', () => {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
    initParticles();
});

// Capture mouse movements
window.addEventListener('mousemove', (e) => {
    mouse.x = e.clientX;
    mouse.y = e.clientY;
});

// Clear mouse coordinates when leaving page bounds
window.addEventListener('mouseout', () => {
    mouse.x = null;
    mouse.y = null;
});

class Particle {
    constructor(x, y, size, color, speedX, speedY) {
        this.x = x;
        this.y = y;
        this.size = size;
        this.color = color;
        this.speedX = speedX;
        this.speedY = speedY;
    }
    
    draw() {
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.size, 0, Math.PI * 2, false);
        ctx.fillStyle = this.color;
        ctx.fill();
    }
    
    update() {
        if (this.x > canvas.width || this.x < 0) { this.speedX = -this.speedX; }
        if (this.y > canvas.height || this.y < 0) { this.speedY = -this.speedY; }
        
        this.x += this.speedX;
        this.y += this.speedY;
        
        if (mouse.x !== null && mouse.y !== null) {
            let dx = mouse.x - this.x;
            let dy = mouse.y - this.y;
            let distance = Math.sqrt(dx * dx + dy * dy);
            
            if (distance < mouse.radius) {
                const force = (mouse.radius - distance) / mouse.radius;
                const dirX = dx / distance;
                const dirY = dy / distance;
                
                this.x -= dirX * force * 3;
                this.y -= dirY * force * 3;
            }
        }
    }
}

function initParticles() {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight;
    
    particlesArray = [];
    const numberOfParticles = Math.min((canvas.width * canvas.height) / 11000, 100);
    
    for (let i = 0; i < numberOfParticles; i++) {
        const size = Math.random() * 2 + 1;
        const x = Math.random() * (canvas.width - size * 2) + size;
        const y = Math.random() * (canvas.height - size * 2) + size;
        
        const speedX = (Math.random() * 0.4 - 0.2);
        const speedY = (Math.random() * 0.4 - 0.2);
        
        const color = document.body.classList.contains('light-theme') 
            ? 'rgba(123, 44, 191, 0.15)' 
            : 'rgba(0, 245, 212, 0.2)';
            
        particlesArray.push(new Particle(x, y, size, color, speedX, speedY));
    }
}

function connectParticles() {
    let opacityValue = 1;
    const maxDistance = 140;
    
    for (let a = 0; a < particlesArray.length; a++) {
        for (let b = a; b < particlesArray.length; b++) {
            let dx = particlesArray[a].x - particlesArray[b].x;
            let dy = particlesArray[a].y - particlesArray[b].y;
            let distance = Math.sqrt(dx * dx + dy * dy);
            
            if (distance < maxDistance) {
                opacityValue = 1 - (distance / maxDistance);
                
                const lineColor = document.body.classList.contains('light-theme')
                    ? `rgba(123, 44, 191, ${opacityValue * 0.08})`
                    : `rgba(157, 78, 221, ${opacityValue * 0.12})`;
                    
                ctx.strokeStyle = lineColor;
                ctx.lineWidth = 1;
                ctx.beginPath();
                ctx.moveTo(particlesArray[a].x, particlesArray[a].y);
                ctx.lineTo(particlesArray[b].x, particlesArray[b].y);
                ctx.stroke();
            }
        }
    }
}

function animateParticles() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    
    for (let i = 0; i < particlesArray.length; i++) {
        const isLight = document.body.classList.contains('light-theme');
        particlesArray[i].color = isLight ? 'rgba(123, 44, 191, 0.25)' : 'rgba(0, 245, 212, 0.3)';
        
        particlesArray[i].update();
        particlesArray[i].draw();
    }
    
    connectParticles();
    requestAnimationFrame(animateParticles);
}

// Initialise particles system
initParticles();
animateParticles();
